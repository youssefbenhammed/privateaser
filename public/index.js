//Set the booking price 

//Attention : Modifie les variables car constantes
function setBookingPrice(events,bars) {
  var reduction=0; // Réduction a appliqué en pourcentage : entre 0 et 1 
  events.forEach(event=>{
    bars.forEach(bar => {
      if (event.barId==bar.id) {

        if( 10<=event.persons && event.persons<20) {
          reduction=0.10;
        }
        if(20<=event.persons && event.persons<60) {
          reduction=0.30;
        }
        if(60<=event.persons) {
          reduction=0.50;
        }
        event.price = event.time*bar.pricePerHour + event.persons*bar.pricePerPerson; // Calcul du prix sans réduction
        event.price = event.price*(1-reduction); // Calcul de prix après réducion 
      }
    });
  });
}

function setCommission(events) {
 events.forEach(event=>{
   var commission = (event.price*0.3);
   var insurance = commission/2;
   var treasury = -1;
   if(insurance>event.persons){ // Verification que l'opération peut se faire .
      treasury=event.persons;
   }
   var privateaser = commission-insurance-treasury;

   if(treasury!=-1){ // Si le calcul précédent a pu se faire on modifie dans la liste sinon on log une erreur.
     event.commission.insurance=insurance;
     event.commission.treasury=treasury;
     event.commission.privateaser=privateaser;
   }
   else{
     console.log("Erreur calcul commission sur la reservation numero " + event.eventId);
   }

 });
}

